void bubbleSort(int *arr,uint32_t len) {
    int  i,j,temp ;
    for (  i = 0; i < len - 1; i++) {
        for (  j = 0; j < len - 1 - i; j++) {
            if (arr[j] > arr[j+1]) {        // 相邻元素两两对比
                temp = arr[j+1];        // 元素交换
                arr[j+1] = arr[j];
                arr[j] = temp;
            }
        }
    }
}
